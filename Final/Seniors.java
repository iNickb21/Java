public class Seniors
{
    private String name; 
    private double gpa;
    
    public Seniors(String n, double g)
    {
        name = n;
        gpa = g;
    }
    
    public String getName()
    {
        return name;
    }
    public double getGpa()
    {
        return gpa;
    }
}
